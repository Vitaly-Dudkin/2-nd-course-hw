// В вашей компании пятница является отчетным днем. Нужно написать программу, которая:
// считает дни месяца по датам, определяет, какой день пятница,
// выводит сообщение с напоминанием, что нужно подготовить еженедельный отчет. Условия задачи:
// Создайте переменную, которая хранит в себе номер первой пятницы месяца (число от 1 до 7).
// Выведите на каждую пятницу месяца (включая полученную) сообщение следующего вида: "Сегодня пятница, ...-е число. Необходимо подготовить отчет."
// Исходите из того, что в нашем месяце 31 день. Должно вывестись от 4 до 5 сообщений с напоминаниями по разным датам.

let firstFriday = 5; // Начинаем с 3-го числа (пятница)
let dayInMonth = 1; 
let month = 1;

while (month <= 12) {
    if (firstFriday === 7) {
        console.log(`Сегодня пятница, ${dayInMonth}-е число. Необходимо подготовить отчет.`);
        firstFriday = 1; // Сбрасываем на 1, так как неделя начинается с понедельника
    } else {
        firstFriday++;
    }

    dayInMonth++;

    // Проверяем, если месяц имеет 31 день
    if ((month === 1 || month === 3 || month === 5 || month === 7 || month === 8 || month === 10 || month === 12) && (dayInMonth > 31)) {
        dayInMonth = 1;
        month++;
        console.log(`Переход на следующий месяц: ${month}`);
    }
    
    // Проверяем, если месяц имеет 30 дней
    if ((month === 4 || month === 6 || month === 9 || month === 11) && (dayInMonth > 30)) {
        dayInMonth = 1;
        month++;
        console.log(`Переход на следующий месяц: ${month}`);
    }

    // Проверяем февраль, который имеет 28 дней (не учитываем високосные годы)
    if (month === 2 && dayInMonth > 28) {
        dayInMonth = 1;
        month++;
        console.log(`Переход на следующий месяц: ${month}`);
    }
}

