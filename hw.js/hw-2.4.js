// Задание 5
// Дано число n=1000. Ваша задача — делить его на 2 до тех пор, пока результат деления не станет меньше 50.
// Определите, какое число получится в результате. Вычислите количество итераций (проходов цикла),
// которые потребовались для достижения результата, и запишите это количество в переменную num

let n = 1000;
num = 0;
while (n > 50) {
    n = n / 2
    num++
    if (n < 50) {
        console.log(`Число получится в результате ${n}, количество итераций - ${num}`)
        break
    }
}


