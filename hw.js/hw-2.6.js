// Задание 4
// С помощью вложенных циклов создайте многомерный массив вида: [[1, 1, 1], [1, 1, 1], [1, 1, 1]]
const rows = 3; // количество строк
const cols = 3; // количество столбцов

// Инициализация многомерного массива
const multiArray = [];

// Внешний цикл для строк
for (let i = 0; i < rows; i++) {
    const innerArray = []; // Создаем новый внутренний массив для каждой строки

    // Внутренний цикл для столбцов
    for (let j = 0; j < cols; j++) {
        innerArray.push(1); // Заполняем внутренний массив значением 1
    }

    multiArray.push(innerArray); // Добавляем внутренний массив в многомерный массив
}

console.log(multiArray);


